     .MODEL SMALL
.STACK 100h

.DATA
num DW 5
result DW ?

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV AX,num
    CALL FACT
    MOV result,AX
    MOV AH,4Ch
    INT 21h
MAIN ENDP

FACT PROC
    CMP AX,1
    JLE B
    PUSH AX
    DEC AX
    CALL FACT
    POP BX
    MUL BX
    RET
B:
    MOV AX,1
    RET
FACT ENDP
END MAIN
